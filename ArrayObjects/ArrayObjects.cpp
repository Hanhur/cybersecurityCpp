// Массив объектов, конструктор которых принимает аргумент

#include <iostream>
using namespace std;

class demo {
    int a;
    public:
        demo(int n)
        {
            a = n;
        }
        int get_a()
        {
            return a;
        }
};

int main()
{
    demo ar[3] = {3, 2, 1};
    int i;

    for (i = 0; i < 3; i++) cout << ar[i] .get_a() << " ";
    cout << endl;

    return 0;
}